package IntExample;

public class IntExample {
    public static void main(String[] args) {
        int var1 = 10; //10진수 표현
        int var2 = 012; //8진수 표현: 앞에 0붙임
        int var3 = 0xA; //16진수 표현: 앞에 0x붙임
        
        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var3);
    }
}
